/**
 * v2.9.1: 개인정보 자동 마스킹 (#4) — AI 분석 전 적용. 누출 방지 우선(과마스킹 허용).
 *   제거: 전화/주민/계좌/카드/이메일/차량번호/주소(일부)/이름(3자+님·씨)
 */
export function maskPII(input) {
  let t = String(input || '');
  const stats = {};
  const sub = (re, label) => { t = t.replace(re, () => { stats[label] = (stats[label] || 0) + 1; return label; }); };

  // 이메일
  sub(/[A-Za-z0-9._%+\-]+@[A-Za-z0-9.\-]+\.[A-Za-z]{2,}/g, '[이메일]');
  // 카드번호 4-4-4-4
  sub(/\b\d{4}[-\s]?\d{4}[-\s]?\d{4}[-\s]?\d{4}\b/g, '[카드번호]');
  // 주민등록번호 6-7
  sub(/\b\d{6}[-\s]?[1-4]\d{6}\b/g, '[주민번호]');
  // 휴대폰
  sub(/\b01[0167-9][-\s.]?\d{3,4}[-\s.]?\d{4}\b/g, '[전화번호]');
  // 일반 전화(지역번호)
  sub(/\b0(2|[3-6][1-5])[-\s.]?\d{3,4}[-\s.]?\d{4}\b/g, '[전화번호]');
  // 차량번호 12가3456 / 123가4567
  sub(/\b\d{2,3}\s?[가-힣]\s?\d{4}\b/g, '[차량번호]');
  // 계좌번호 (3그룹 이상 숫자-하이픈; 전화/주민 마스킹 후)
  sub(/\b\d{2,6}-\d{2,6}-\d{2,7}(-\d{1,6})?\b/g, '[계좌번호]');
  // 주소 (시/도 + 시군구 + 동/읍/면/로/길) — 대략적
  sub(/(서울|부산|대구|인천|광주|대전|울산|세종|경기|강원|충북|충남|전북|전남|경북|경남|제주)[가-힣\s]{0,12}(시|군|구)[가-힣0-9\s]{0,16}(동|읍|면|로|길)\s?\d{0,4}(번지|번길)?/g, '[주소]');

  // 이름 + 님/씨 (성+이름 3자만 → 홍길동님; 선생님·사장님 등 2자 호칭은 제외)
  t = t.replace(/([가-힣]{3})(님|씨)(?![가-힣])/g, (m, name, suf) => {
    stats['[이름]'] = (stats['[이름]'] || 0) + 1;
    return '[이름]' + suf;
  });

  return { text: t, masked: stats };
}
