function norm(s) { return String(s).toLowerCase().replace(/\s+/g, ''); }

function findCompany(input) {
  const key = norm(input);
  if (aliases[key]) return aliases[key];
  if (contacts[input]) return input;
  for (const k in contacts) {
    if (norm(k) === key) return k;
  }
  return null;
}

// 라인 배열 → 1500자 단위 청크 (카톡 sendChunked 동일 로직)
function chunkLines(lines, max) {
  max = max || 1500;
  const out = [];
  let buf = '';
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i] + '\n';
    if (buf.length + line.length > max) { if (buf) out.push(buf.replace(/\n+$/, '')); buf = ''; }
    buf += line;
  }
  if (buf) out.push(buf.replace(/\n+$/, ''));
  return out;
}

